# Код алфавита согласно заданию
alphabet_code = {
    'А': 1, 'Б': 2, 'В': 3, 'Г': 4, 'Д': 5, 'Е': 6, 'Ж': 7, 'З': 8,
    'И': 9, 'Й': 10, 'К': 11, 'Л': 12, 'М': 13, 'Н': 14, 'О': 15,
    'П': 16, 'Р': 17, 'С': 18, 'Т': 19, 'У': 20, 'Ф': 21, 'Х': 22,
    'Ц': 23, 'Ч': 24, 'Ш': 25, 'Щ': 26, '_': 27, 'Ь': 28, 'Ы': 29,
    'Э': 30, 'Ю': 31, 'Я': 0
}

# Информационное слово
info_word = "СТУПНИКОВ"
keys = [10, 22]  # Ключевые слова

# Кодируем слово СТУПНИКОВ десятичным кодом
print("кодирование информационного слова:")
codes = []
print("+-----+------+---------+")
print("| Буква| Код  | Двоичный|")
print("+-----+------+---------+")
for letter in info_word:
    code = alphabet_code[letter]
    codes.append(code)
    binary = format(code, '05b')  # 5 бит для чисел до 31
    print(f"|  {letter}  |  {code:2d}  | {binary} |")
print("+-----+------+---------+")

# Переводим ключи в двоичную систему
print("двоичные ключи:")
key1_bin = format(keys[0], '05b')
key2_bin = format(keys[1], '05b')
print(f"Ключ 10 -> двоичный: {key1_bin}")
print(f"Ключ 22 -> двоичный: {key2_bin}")

# Шифрование (XOR с ключами поочередно)
print("процесс шифрования (xor по модулю 2):")
print("+-----+------+---------+---------+---------+---------+")
print("| Буква| Код  | Двоичный|  Ключ   | Результат|Зашифр. |")
print("|     |      |   кода  |         |   XOR    |  код   |")
print("+-----+------+---------+---------+---------+---------+")

encrypted_codes = []
for i, (letter, code) in enumerate(zip(info_word, codes)):
    # Определяем ключ для текущей позиции (поочередно)
    current_key = keys[i % 2]
    key_bin = format(current_key, '05b')
    
    # Выполняем XOR
    xor_result = code ^ current_key
    xor_bin = format(xor_result, '05b')
    
    encrypted_codes.append(xor_result)
    
    print(f"|  {letter}  |  {code:2d}  | {format(code, '05b')} |   {key_bin}  |   {xor_bin}  |   {xor_result:2d}   |")

print("+-----+------+---------+---------+---------+---------+")